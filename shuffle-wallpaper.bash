#!/bin/bash

#
#  Name: shufflewallpaper.bash
#  Author: Larry Esmonde
#  Date: 5 Oct 2024
#  Purpose: Shuffle images and apply as wallpaper every N minutes
#
tarfile=/tmp/setDesktopWallpaper.tgz
workflowdir=/tmp/setDesktopWallpaper.workflow

#
#  Set default delay to 1 hour between switching
#
delaysecs=3600

####################################################################
# begin function defs
####################################################################

#
#  usage()
#
usage() {
    echo -e "usage: $0 [-d <delay>[hms]] [<image-dir>]\n\n  Examples:\n    $0 -d 4 \$HOME/Photos    # switch every 4 hours\n    $0 -d 2h .              # switch every 2 hours\n    $0 -d 30m /tmp/imagedir # switch every 30 mins\n    $0 -d 45s ~/Pictures    # switch every 45 secs\n\n  Default switch time is 1 hour and default time unit is hours"
    exit 0
}

#
#  setdelay()
#
#  Reads the delay parameter and sets the delay in seconds between
#  wallpaper switches
#
setdelay() {
    #
    #  Use regex to determine what value and units
    #  were passed in, and calculate accordingly
    #
    pstr="$1"

    if [[ "$pstr" =~ ^[0-9]0-9]*$ ]]; then

        delaysecs=$(($pstr * 3600))

    elif [[ "$pstr" =~ ^[0-9][0-9]*[hms]$ ]]; then

        # process units first
        unitchar="${pstr:0-1}"
#        pval="${pstr::-1}"
        pval="${pstr:0:${#pstr}-1}"

        case $unitchar in
            h)
                delaysecs=$(($pval * 3600))
                ;;
            m)
                delaysecs=$(($pval * 60))
                ;;
            s)
                delaysecs=$pval
                ;;
            *)
                echo "ERROR: Unknown time unit [$unitchar] in param [$pstr]"
                echo "ERROR: Cannot continue."
                exit 1
                ;;
        esac

    else
        echo "ERROR: Invalid time pattern [$1]"
        echo "ERROR: Cannot continue."
        exit 1
    fi
}
#
#  setwallpaper()
#
#  Use the Automator workflow to set the wallpaper to the image
#  file passed in as parameter
#
setwallpaper() {
    if [ "$@" != "" ]; then
        automator -i "$@" "$workflowdir"
    fi

    return
}

