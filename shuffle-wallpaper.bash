####################################################################
# main()
####################################################################

#
#  Process switch parameters
#
while getopts "hHd:" o; do
    case "${o}" in
        h|H)
            usage
            exit 0
            ;;
        d)
            if [[ "$OPTARG" =~ ^[0-9][0-9]*$ || "$OPTARG" =~ ^[0-9][0-9]*[hms]$ ]]; then
                setdelay ${OPTARG}
            else
                echo "ERROR: Invalid time pattern [$OPTARG]"
                echo "ERROR: Cannot continue"
                exit 1
            fi
            ;;
        *)
            usage
            ;;
    esac
done

#
#  Shift off the switch parameters, leaving the positional args
#
shift $((OPTIND-1))

#
#  Verify source directory exists
#

idir="$@"

#
#  If image dir is not provided, default for current dir
#
[ -z "$idir" ] && idir="."

if [ ! -d "$idir" ]; then
    echo "ERROR: Unable to locate image dir [$idir]"
    echo "ERROR: Cannot continue."
    exit 1
fi


#
#  Unpack the Automator workflow into /tmp
#
unpackworkflow

cd "$idir"

#
#  Loop forever, randomizing each time through
#
while [ 1 = 1 ]; do

    #
    #  Get the shuffled list of files from the image dir
    #
    #  (use the space-replacement trick to handle files with
    #   spaces in their names by changing spaces in filenames
    #   to backticks)
    #
    for file in $(\ls -1 | egrep "([Jj][Pp][Gg]|[Hh][Ee][Ii][Cc]|[Pp][Nn][Gg])" | sort -R | sed 's/\.\([Jj][Pp][Gg]\) /.\1,/g' | sed 's/\.\([Pp][Nn][Gg]\) /.\1,/g' | sed 's/ /`/g' | sed 's/,/ /g'); do
        #
        #  Change the back-ticks back to spaces
        #
        ffile=$(echo $file | sed 's/`/ /g')

        #
        #  Call the setwallpaper() function to change the wallpaper
        #
        setwallpaper "$ffile"

        #
        #  Sleep however long was specified, or the default
        #
        sleep $delaysecs

    done
done

exit 0
